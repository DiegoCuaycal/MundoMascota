    public Producto() throws SQLException {
        initComponents();

        modeloProducto = new DefaultTableModel();
        modeloProducto.addColumn("ID");
        modeloProducto.addColumn("Nombre producto");
        modeloProducto.addColumn("Proveedor");
        modeloProducto.addColumn("P. Compra unitario");
        modeloProducto.addColumn("P. Venta unitario");
        modeloProducto.addColumn("Cantidad");
        this.tblProducto.setModel(modeloProducto);

        this.cbxProveedorProducto.removeAllItems();
        String[] datos = new String[6];
        try {
            Connection con = DriverManager.getConnection("jdbc:postgresql://localhost:5432/conectar", "postgres", "123");
            Statement Sent = con.createStatement();
            ResultSet rs = Sent.executeQuery("select * from proveedor");
            while (rs.next()) {
                this.cbxProveedorProducto.addItem(rs.getString("nombre_proveedor"));
            }
            ResultSet resultado = Sent.executeQuery("select * from producto");
            while (resultado.next()) {
                datos[0] = resultado.getString(1);
                datos[1] = resultado.getString(2);
                datos[2] = resultado.getString(3);
                datos[3] = resultado.getString(4);
                datos[4] = resultado.getString(5);
                datos[5] = resultado.getString(6);
                modeloProducto.addRow(datos);
            }
            tblProducto.setModel(modeloProducto);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

