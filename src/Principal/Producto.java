    private void btnEliminarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProveedorActionPerformed

        // TODO add your handling code here:
        if (tblProducto.getSelectedRow() >= 0) {
            DefaultTableModel tm = (DefaultTableModel) tblProducto.getModel();
            int rpta = JOptionPane.showConfirmDialog(this, "Deseas eliminar el Producto seleccionado", "", JOptionPane.INFORMATION_MESSAGE);
            if (rpta == JOptionPane.YES_OPTION) {
                tm.removeRow(tblProducto.getSelectedRow());
            }
        } else {
            JOptionPane.showConfirmDialog(this, "Seleccione el Producto que desea eliminar", "", JOptionPane.WARNING_MESSAGE);

        }

    }//GEN-LAST:event_btnEliminarProveedorActionPerformed

