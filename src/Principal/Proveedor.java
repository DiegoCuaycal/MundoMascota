    private void btnEliminarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProveedorActionPerformed
        // TODO add your handling code here:
        if (tblProveedor.getSelectedRow() >= 0) {
            DefaultTableModel tm = (DefaultTableModel)tblProveedor.getModel();
            int rpta = JOptionPane.showConfirmDialog(this, "Deseas eliminar el Proveedor seleccionado","",JOptionPane.INFORMATION_MESSAGE);
            if (rpta == JOptionPane.YES_OPTION) {
                tm.removeRow(tblProveedor.getSelectedRow());
            }
        }else{
            JOptionPane.showConfirmDialog(this, "Seleccione el Proveedor que desea eliminar","",JOptionPane.WARNING_MESSAGE);
        }        

    }//GEN-LAST:event_btnEliminarProveedorActionPerformed

