    private void btnActualizarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarProveedorActionPerformed
        // TODO add your handling code here:
        String[] info = new String[5];
        info[0] = txtRucProveedor.getText();
        info[1] = txtNombreProveedor.getText();
        info[2] = txtTelefonoProveedor.getText();
        info[3] = txtCorreoProveedor.getText();
        info[4] = txtProductoProveedor.getText();

        for (int i = 0; i < tblProveedor.getColumnCount(); i++) {
            modeloProveedor.setValueAt(info[i], filas, i);
        }
        
    }//GEN-LAST:event_btnActualizarProveedorActionPerformed

