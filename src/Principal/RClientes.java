    private void btnActualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarClienteActionPerformed

        // TODO add your handling code here:
        String[] info = new String[5];
        info[0] = txtCedulaCliente.getText();
        info[1] = txtNombreCliente.getText();
        info[2] = txtApellidoCliente.getText();
        info[3] = txtTelefonoCliente.getText();
        info[4] = txtDireccionCliente.getText();

        for (int i = 0; i < tblCliente.getColumnCount(); i++) {
            modeloCliente.setValueAt(info[i], filas, i);
        }

    }//GEN-LAST:event_btnActualizarClienteActionPerformed

