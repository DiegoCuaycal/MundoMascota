    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        if (tblCliente.getSelectedRow() >= 0) {
            DefaultTableModel tm = (DefaultTableModel) tblCliente.getModel();
            int rpta = JOptionPane.showConfirmDialog(this, "Deseas eliminar el Cliente seleccionado", "", JOptionPane.INFORMATION_MESSAGE);
            if (rpta == JOptionPane.YES_OPTION) {
                tm.removeRow(tblCliente.getSelectedRow());
            }
        } else {
            JOptionPane.showConfirmDialog(this, "Seleccione el Cliente que desea eliminar", "", JOptionPane.WARNING_MESSAGE);

        }

    }//GEN-LAST:event_btnEliminarClienteActionPerformed

